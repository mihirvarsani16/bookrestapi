package com.bookrestapi.bookrestapi;

public class Calculater {

    // sum
    public int doSum(int a, int b, int c) {
        return a + b + c;
    }

    // product
    public int doMulti(int a, int b) {
        return a * b;
    }

    // compare
    public Boolean compareTwoNums(int a, int b) {
        return a == b;
    }
}
